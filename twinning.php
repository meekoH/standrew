                    <h1>Twinning</h1>

                    <p>The Saskatoon Native Circle Ministry was established by The Presbyterian Church in Canada to serve in an urban environment of poverty, broken families and lost connections to native traditions. In 2001, The Church of St. Andrew and St. Paul joined the Saskatoon Ministry in a twinning relationship, a formal partnership between two congregations or groups, working together mutually and equally as members of the Body of Christ. The goal is a relationship in which each learns and enriches the other as we witness the gospel and work together on a specific project or exchanges of information or skills.</p>

                    <h3>Healing and Reconciliation</h3>

                    <img src="http://images.unoapp.com/boxdata/asset30718/images/twinning.jpg" class="quiet-garden-img" style="max-width:50%;margin:0 15px 15px 0;">
                    
                    <p>This springtime event in the Quiet Garden is always a joyous moment, with friends new and old, tastes of bannock, wild rice and Labrador tea, and the rhythms of our native brothers and sisters.</p>

                    <p>This year, we were privileged to be joined by Nina Segalowitz and Taqralik Partridge, Inuit throat singers, whose music and personalities delighted young and old. Enjoy a moment with Nina and Taqrilak <a href="https://www.facebook.com/stAstP/videos/1126817284035147/" target="_blank">here</a>.</p>

                    <p>Healing and Reconciliation is a Ministry of the Presbyterian Church in Canada built on the Great Commandment: Love the Neighbour. At the heart of Healing and Reconciliation is the belief that society will be transformed for the better when Aboriginal and non-Aboriginal people develop a new understanding of each other and build a new relationship founded on mutual respect and love of neighbour.</p>

                    <p>"With God's guidance, our church will seek opportunities to walk with Aboriginal peoples to find healing and wholeness together as God’s people." – from the Church’s Confession to Aboriginal Peoples.</p>

                    <h3 style="clear:both;">What is the Native Circle Ministry?</h3>

                    <img src="img/twinning-img1.jpg" class="quiet-garden-img" style="max-width:50%;margin:0 15px 15px 0;">

                    <p>The Native Circle Minister, the Reverend Stewart Folster, a graduate of the Vancouver School of Theology, is one of three active ordained Native Presbyterian ministers in Canada. His store-front mission in Saskatoon, provides ministry, Bible study, visitation, caring and special services for funerals and weddings.  His ministry also includes youth and addiction counselling for those who, like the Rev. Folster himself, knew childhoods marred by violence, alcoholism and extreme poverty.</p>

                    <h3 style="clear:both;">Neechewagon (Nee-chay-wa-gun)</h3>
                    <p>This Cree word means "brothers and sisters," and "friends" in the Native language.  The A &amp; P Neechewagon is a group of members of our church who meet regularly to learn more about Native culture, history and spirituality and to nourish our twinning with the Saskatoon Native Circle Ministry. Through the generosity of many in our congregation and the East Toronto Presbytery, we were blessed to buy a van for the Ministry.</p>

                    <h3>Knitters for Neechewagon</h3>

                    <img src="img/twinning-img2.jpg" class="quiet-garden-img" style="max-width:50%;margin:0 15px 15px 0;">
                    <p>Saskatoon winters are cold, and especially bleak for the homeless and poor. A group of ladies at St. Andrew and St. Paul gather once a month to knit mittens, scarves, sweaters and caps for those cared for by the Saskatoon Native Circle Ministry. These devoted ladies have produced hundreds of items over the past four years, all shipped with love to our friends in Saskatoon.</p>
