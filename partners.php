                        <p>In the early 1980's more and more women were seen sleeping in doorways, and begging in the streets of Montreal. Other women first noticed this worrisome trend. A group of parishioners working within the inter-faith committee commissioned the Reverend Donna Wilson of Christ Church Cathedral to study the problem. The report that resulted, showed that there was a growing network of shelters for homeless men, and for battered women, but that the services available to homeless women did not meet increasing needs. This led to the founding of Auberge Madeleine, a shelter for women who are alone and in difficulty, or homeless, in Montreal.</p>
                        
