                    <h1>Easter &amp; Lent</h1>

                    <p>The season of Lent is a time for reflection and meditation on the sacrifice that Jesus Christ, the Son of God, made for the world. His death for our sins and His rising again for our new life shape this season, which begins with sober reflection and culminates in the joyous celebration of the Resurrection. We invite you to share the observance of Lent and Easter with us at The Church of St. Andrew and St. Paul.</p>

                    <h3>The First Sunday of Lent</h3>
                    <p>The Sacrament of Holy Communion is observed on March 13, the first Sunday in Lent. At the Church of St. Andrew and St. Paul all those professing the Christian faith are invited to take communion.</p>

                    <img src="img/easter-img1.jpg" style="float:left;max-width:45%;margin:0 10px 10px 0;clear:both;border:2px solid #149371;">
                    <h3>Midweek Chapel</h3>
                    <p>Each Thursday in Lent there is a brief service at 12:30 pm in the Chapel. You are invited to join others in prayer and reflection. There is a short devotional message from the Minister. Lunch is served afterward at a modest cost. The whole service is designed to allow business people and others to withdraw for a short time from the hustle and bustle of the day without disturbing normal noon hour schedules and without missing lunch.</p>
                    
                    <img src="img/easter-img2.jpg" style="float:left;max-width:45%;margin:0 10px 0 0;clear:both;border:2px solid #149371;">
                    <h3>Palm Sunday</h3>
                    <p>On Palm Sunday, April 17, we commemorate the triumphal entry of the humble Saviour into Jerusalem, greeted by crowds carrying palm branches. As a symbol of this, the children of the congregation receive palm fronds. New members of the congregation take their vows during this service, which is followed by a reception to which all are invited.</p>

                    <h3>Maundy Thursday</h3>
                    <p>At 6:00 pm on Maundy Thursday in Holy Week, April 21, the Sacrament is observed at dusk and is followed by the ancient liturgy of Tenebrae, which is one of the most beautiful and moving services of the Church year. The bread and wine are placed on a special Table in the unlit chancel. At the end of the Communion service, texts relating our Lord's Passion are read by elders. As each reading ends, a Communion candle on the Table is snuffed out and the church darkens as a choral response is heard from the gallery. At the last reading, with the final candlelight gone, the church is left "without light" as it seemed to the disciples after the crucifixion. In recognition of the promise of Easter to come, the Minister then lights the Christ candle and the congregation leaves the sanctuary in silence.</p>

                    <h3>Good Friday</h3>
                    <p>Good Friday morning, April 22, at 11 a.m. we share in a service of hymns and reflections on Jesus' words from the cross. At 8:00 pm, the choir and a baroque orchestra presents its moving interpretation of a major choral work.</p>

                    <h3>Easter Sunday</h3>
                    <p>Easter Sunday, April 24, sees the Narthex and the chancel resplendent with lilies and spring flowers to greet the Resurrection Day of Our Lord, and Holy Communion is once again celebrated. On this joyous occasion, the bread and wine are brought in procession by the elders through the congregation to be put on the Communion Table. This is a choral communion with wonderful music by the choir, expressing the festive nature of this day. At the end of the service the flowers are delivered to those members of the congregation who are shut-in or ill.</p>

