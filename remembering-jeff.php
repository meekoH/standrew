<p>The Reverend Jeff Veenstra, 1960 - 2015</p>

<p>With affection and gratitude, we remember our Minister, The Reverend Jeff Veenstra, friend to many and brother in Christ to us all. Jeff was inducted as Minister of The Church of St. Andrew and St. Paul on September 12, 2010, and served this congregation with humility, patience, wisdom, and grace until his death on May 2, 2015.</p>

